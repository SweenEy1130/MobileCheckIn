CREATE TABLE DETECT(
    OWNER BIGINT UNSIGNED NOT NULL,
    FOREIGN KEY(OWNER) REFERENCES USER(UID) ON DELETE CASCADE ON UPDATE CASCADE,
    SESSIONID BIGINT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,
    AUDIOHASH CHAR(100),
    AUDIODETECT TINYINT,
    LONGITUDE DOUBLE,
    LATITUDE DOUBLE,
    FACEHASH CHAR(100),
