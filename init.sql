CREATE TABLE USER(
    UID BIGINT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,
    USERNAME CHAR(20) ,
    PASSWORD CHAR(20) ,
    CHINAME CHAR(20),
    LOCID INTEGER,
    IMAGESAMPLE CHAR(100),
    AUDIOENGINE CHAR(100),
    CREATETIME DATETIME,
    DEPARTMENT CHAR(100),
    );
ALTER TABLE USER ADD UNIQUE(USERNAME);
